// Array Methods Class Assignment : (POP)(PUSH)(SHIFT)(UNSHIFT)(SLICE)(SPLICE)
let arr = ["Anas","Talha","Rehman","Hakeem"]
console.log(arr.pop()) // Array POP method last Index Remove
console.log(arr.push("Ali")) // Array PUSH method last Index Add
console.log(arr.shift()) // Array shift method First Index Remove
console.log(arr.unshift("Khan")) // Array unshift method First Index Add
console.log(arr.slice(1,3)) // Array slice method  Two Parameters 
console.log(arr.splice(1,3,"Haider","Bhai")) // Array splice method  Three Parameters 

